package ex_03_Variable_Datatypes;

public class Lab019_Variable {
    public static void main(String[] args) {
        int a = 10;
        System.out.print("print the message without new line");
        System.out.println("Hello World");
        System.out.println("Print the message with new line");
        System.out.printf("Hello A value is %d",a);
        // %d -> int , byte , short , long , - data Type
        // %s -> String
        // %f -> float , double
        // %b -> boolean
    }
}
