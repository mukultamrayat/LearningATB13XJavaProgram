package ex_04_Operators;

public class Lab034_relational_operators {
    public static void main(String[] args) {
        // < less than
        // <= less than or equal to
        // > Greater
        // > = Greater or Equal
        // ==  Equal to (but checking)
        // !=  Not Equal
        // All of them will return boolean output
        int a = 35;
        int b = 30;
        boolean c = b < a;
        System.out.println(c);

        int mukul_age = 30;
        int rashmi_age = 26;
        boolean result = mukul_age <= rashmi_age;
        System.out.println(result);

        int a1 = 10;
        int b1 = 15;
        boolean c1 = b1 > a1;
        System.out.println(c1);

        int d = 12;
        int e = 11;
        boolean f = d >= e;
        System.out.println(f);

        int age = 25;
        int age_1 = 25;
        boolean age_2 = age == age_1;
        System.out.println(age_2);








    }
}
