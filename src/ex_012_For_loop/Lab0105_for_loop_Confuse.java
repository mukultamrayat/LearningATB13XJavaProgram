package ex_012_For_loop;

public class Lab0105_for_loop_Confuse {
    public static void main(String[] args) {
        for (int i = 0; i <=10;i++) {// 0 to 10 Run 11 times
            System.out.println(i);
        }
        for (int j = 0; j < 10;j++){ // 0 to 9 Run 10 times this is the correct approach
            System.out.println(j);
        }
        for (int a = 1; a<=10;a++){// 1 to 10 Run 10 times
            System.out.println(a);
        }
    }
}
