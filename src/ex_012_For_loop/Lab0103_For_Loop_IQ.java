package ex_012_For_loop;

public class Lab0103_For_Loop_IQ {
    // ideally we are not using the long and float in for loop and string is not working for loop
    public static void main(String[] args) {
        for (long l = 0; l < 10; l++){
            System.out.println(l);
        }
        for (float f = 0.001f; f < 10.098f; f++){
            System.out.println(f);
        }
    }
}
