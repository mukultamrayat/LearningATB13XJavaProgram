package ex_011_Switch;

public class Lab095_Interview {
    public static void main(String[] args) {
        char code = 'C';
        switch (code){
            case 'A' :
                System.out.println("A");
                break;
            case 'B' :
                System.out.println("B");
                break;
            default:
                System.out.println("Helloooooooo");

        }

    }
}
